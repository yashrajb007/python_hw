'''This Is Program To Convert A Decimal No To Other Numbering System'''
print("********** Various Numbering System *********")
no=int(input("Enter Any Number = "))
print(f"Given No {no}'s Binary Representation = {bin(no)}")
print(f"Given No {no}'s Octal Representation = {oct(no)}")
print(f"Given No {no}'s Hexadecimal Representation = {hex(no)}")